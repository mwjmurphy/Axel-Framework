package org.xmlactions.mapping;

/**
 * \page axel-mapping AXEL Mapping Tools
 *
 * \section intro_sec Introduction
 *
 * AXEL comes with a simple yet powerful mapping tool for mapping data between XML and Beans.
 * 
 * 
 * 
 * There are two schemas that define the mapping rules for mapping beans to xml and for mapping xml to beans.
 * <ul>
 * 	<li><b>http://xmlactions.org/schema/xml_to_beans.xsd</b> - Mapping from XML to Beans</li>
 * 	<li><b>http://xmlactions.org/schema/bean_to_xml.xsd</b> - Mapping from Beans to XML</li>
 * </ul>
 * These schemas are also stored in the axel-shared-schema project.
 * 
 * Follow this link \ref mapping_beans_to_xml for mapping Beans to XML
 * 
 * Follow this link \ref mapping_xml_to_beans for mapping XML to Beans
 * 
 * Follow this link \ref mapping_json_to_map for mapping JSON to a HashMap
 * 
 * 
 */
