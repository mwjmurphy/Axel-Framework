package org.xmlactions.pager.actions.mapping;

public class MapBean {

    private String name;
    private int number;

    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }
    public void setNumber(int number) {
        this.number = number;
    }
    public int getNumber() {
        return number;
    }
}
